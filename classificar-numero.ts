/*

2) Classificar número
Enunciado: Dado um número n, retorne "Positivo", "Negativo" ou "Zero".

Exemplo:
Entrada: n = -3 → Saída: "Negativo"
Entrada: n = 0 → Saída: "Zero"

*/

const n: number = 0;

if (n < 0) {
    console.log ("Negativo");
}
else if (n > 0) {
    console.log ("Positivo");
}
else {
    console.log("Zero");
}
